import 'package:flutter/material.dart';

class MockItem {
  const MockItem({
    required this.id,
    required this.en,
    required this.so,
    required this.unitEn,
    required this.unitSo,
    required this.price,
    required this.lastCost,
    required this.frequency,
    required this.aliases,
    required this.icon,
  });

  final String id;
  final String en;
  final String so;
  final String unitEn;
  final String unitSo;
  final double price;
  final double lastCost;
  final int frequency;
  final List<String> aliases;
  final IconData icon;

  String name(Locale locale) => locale.languageCode == 'so' ? so : en;
  String unit(Locale locale) => locale.languageCode == 'so' ? unitSo : unitEn;

  bool matches(String query) {
    final q = query.trim().toLowerCase();
    if (q.isEmpty) return true;
    final haystack = [
      en,
      so,
      unitEn,
      unitSo,
      ...aliases,
    ].join(' ').toLowerCase();
    return q.split(RegExp(r'\s+')).every(haystack.contains);
  }
}

class MockParty {
  const MockParty({
    required this.id,
    required this.name,
    required this.phone,
    required this.aliases,
  });
  final String id;
  final String name;
  final String phone;
  final List<String> aliases;

  bool matches(String query) {
    final q = query.trim().toLowerCase();
    if (q.isEmpty) return true;
    return [name, phone, ...aliases].join(' ').toLowerCase().contains(q);
  }
}

